    /**
     * Import the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function import()
    {
        Excel::import('pet-adoption.csv', function($reader) {
            foreach ($reader->get() as $pet) {
                Pet::create([
                    'name' => $pet->name,
                    'speciesname' =>$pet->dogs,
                    'sexname' =>$pet->gender
                ]);
             }
       });
       return Pet::all();
    }
